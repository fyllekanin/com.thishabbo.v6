          We also seen the introduction of our new department - Habbo events. With this layout came a brand new unreleased habbo furni section dedicated to providing you with hotel updates before they appeared on Habbo! However, it didn't take long for ThisHabbo to bottle the department - the community wasn't happy with it and the department faded away. ThisHabbo then reverted back to an older style layout. At this point, people were bored of V4 and wanted something new. Statistics were down - people weren't posting and even better, MSN had closed making it even harder for ThisHabbo to survive.<br />

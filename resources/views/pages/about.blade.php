          <br />
          While we weren't directly using vBulletin on V5, we still relied on its database design. Version 6 was a completely blank page, this allowed us to build a system that fits our needs, and our needs only and increase the speed and reliability of the site.<br />
          <br />
          <a href="/profile/Bear94" class="web-page">Optra</a> (Bear94) was initially the mastermind behind V6. He built the foundations of the project. However, he later became busy with life, so the baton was handed on to <a href="/profile/Stevey" class="web-page">Stevey</a> and <a href="/profile/Andy" class="web-page">Andy</a>. With <a href="/profile/Dez" class="web-page">Dez</a>'s guidance, the boys built on the strong foundations and added the many features and functionality that you see on the site today; including mystery boxes, customisable profiles, daily quests, notices and much more.<br />
          <br />
          As V6 was a complete rewrite for us, it has been built with longevity in mind. We don't anticipate such a big change in the future. Instead, we intend to continually improve and evolve Version 6. We will be releasing new features and design changes all the time. Version 6 will never feel old.<br />
          <br />
          Version 6 also saw us tweak our staff structure, bringing in 'Community Administrators', primarily responsible for the entire community and 'Site Administrators', with their main responsibility being to ensure that the community gets exactly what they need from the site.

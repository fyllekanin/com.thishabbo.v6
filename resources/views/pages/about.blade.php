          ThisHabbo - The Start
        </div>
        <div class="content-ct">
          ThisHabbo is a Habbo fansite, currently owned by <b>irDez</b>. Originally in 2010 the site was owned by <b>Brettles</b>, with <b>irDez</b> and <b>Farmering</b> as radio managers. Back then, it was a shake for fansites as Dez and Farmer were probably the best Radio Managers at the time. However, he bottled out before launch and handed the site over to Dez and Farmer.<br />
          <br />
          On <b>April 30th 2010</b> ThisHabbo launched their forum, waiting almost a month to launch the site on <b>May 22nd 2010</b>! The site was given praise from all angles, everybody seemed to love the layout, and the community kept getting bigger every day! ThisHabbo was going very well and the fanbase was amazing, with a fantastic amount of support from every direction. With this new community continuing to grow and change, something new was needed.<br />
          <br />
          Only two months after Version 1 was launched, the pigs decided to change it up and launch a brand new layout, Version 2! The forum skin went live in early <b>July 2010</b>, with the main site launching on the <b>18th July 2010</b>. This was said to be a massive improvement from their first layout, but it wasn't enough to keep them going. On the <b>15th August 2010 Farmer</b> resigned from his position as site owner leaving Dez to run the site on his own. The pressure put onto him was immense, and just under two weeks after, on the <b>28th August 2010</b>, Dez decided he couldn't handle it any more and closed down the site.
        </div>

        <div class="content-ct">
          <center><i>V1 sucked - V2 wasn't bad - V3 was brilliant - V4 was disappointing - V5 was fantastic.</i></center><br />
          <img src="/_assets/img/website/site_versions/version5.jpg" align="right" style="width: 100px; height: 100px; margin-left: 5px;" class="hover-box-info" title="Click to open full preview." onclick="version5();" /><b>Version 4 had been out for four years and people were sick of it</b>. ThisHabbo had been left behind alongside HFFM and Habbox as newer sites opted not to use vBulletin and code their own systems to manage their content better and more dynamically. This led to the older fansites not being used as much. Fansites were more focused on guides and badges - something ThisHabbo, HFFM and Habbox didn't provide.<br />
          <br />
          Dez had returned from his 2 year of inactivity, scaring the current community (who had no idea who this Dez bloke was) that things would go back to it's depths - the staff stealing, the drama - however, he came back with one plan: Version 5. This would be a shock to the older fansites - because if ThisHabbo moved to something better, then they'd be forced to do something similar.<br />
          <br />
          Dez began designing V4.6. However, he was stuck on a query and some guy called <a href="/profile/Bear94" class="web-page">Optra</a> (Bear94) helped him with it. Instantly, Dez offered him a place on the Admin team and they began to talk. Eric had been at fansites before - but had never really taken on an Administrator role - ThisHabbo was the perfect site to show his work off.<br />
          <br />
          ...And on that day V4.6 was rebranded to V5. <b>September 3rd 2015</b> development began on Version 5. Dez wanted TH, THF and the staff panel all as one in V2, V3 and V4 but back then, we didn't have the ability to make that change - until Eric stressed 'easy'. Dez then decided that V5 will only be 'our best layout' if our longest standing members had an influence on it. At that point, ThisHabbos most loyal members were given V5 exclusive access. They tested and tested - to try and make the release as smooth as possible.<br />
          <br />
          They set a date of launching it on ThisHabbos 5th birthday - but the work was just too much. It was pushed back to the first weekend of January. Guess what? It was too much work!<br />
          <br />
          This led to months of Dez designing <i>(and getting on Erics nerves)</i> - and Eric coding. <b>Version 5 was finally launched early March</b> <i>(when stated it would come early January, but nothing ever goes to plan here... does it?)</i> and this one site contained 4 things:<br />
          - Badges<br />
          - Guides<br />
          - Staff Panel<br />
          - Forum<br />
          <br />
          The goal for V5 was to make it better than older layouts and increase posting statistics. ThisHabbo decided to move all it's staff from Skype and keep everything exclusive to V5. <b>V5 also launched with the return of V2, V3 and V4 skins</b> to ensure everybody liked at least one skin.<br />
          <br />
          ThisHabbos Version 5 hopes to be the stepping stone in fansites. It's slick, fast and pretty damn neat.<br />
          <br />
          ThisHabbo has seen an increase in activity and posts since V5 was first announced, with a lot of older members wanting to get involved with such a interesting layout launch.<br />
          <br />
          <b>The newest tagline for ThisHabbo's Version 5 (with thanks to <a href="/profile/Dan" class="web-page">Dan</a>). "Version 5 doesn't belong to ThisHabbo, it belongs to you, because its customisable to you" and where V5 takes us from here, only time will tell!</b>

          Even with the success of Version 4, The Administration team weren't happy and wanted to create a more user friendly content packed site for our users to enjoy. On the <b>13th May 2013</b> Dez launched the new and improved Version 4.2 packed with new content and unique ideas. This site design was much cleaner than the previous and everything was user-friendly and more easy to navigate.<br />
